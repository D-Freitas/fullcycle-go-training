package patternvalidator

import "regexp"

func EmailValidator(email string) bool {
	emailRegexp := regexp.MustCompile("[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?")
	isMatch := emailRegexp.MatchString(email)
	return isMatch
}
